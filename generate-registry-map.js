const fs = require('fs');
const path = require('path');

const registryDir = path.join(__dirname, 'registry', 'components');
const outputFile = path.join(__dirname, 'registry', 'source-map.ts');

const map = {};

if (fs.existsSync(registryDir)) {
  const categories = fs.readdirSync(registryDir, { withFileTypes: true });
  for (const category of categories) {
    if (category.isDirectory() && category.name !== 'native-haptics') {
      const tsxPath = path.join(registryDir, category.name, `${category.name}.tsx`);
      const jsxPath = path.join(registryDir, category.name, `${category.name}.jsx`);
      
      const toCamelCase = (str) => {
        return str.split('-').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join('');
      };
      
      const componentName = toCamelCase(category.name);

      if (fs.existsSync(tsxPath)) {
        map[componentName] = fs.readFileSync(tsxPath, 'utf8');
      } else if (fs.existsSync(jsxPath)) {
        map[componentName] = fs.readFileSync(jsxPath, 'utf8');
      }
    }
  }
}

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// This file is generated by generate-registry-map.js to bundle component sources for Vercel Serverless Functions.
export const componentSourceMap: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;

fs.writeFileSync(outputFile, content);
console.log('Successfully generated registry/source-map.ts');
