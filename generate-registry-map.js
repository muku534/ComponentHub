const fs = require('fs');
const path = require('path');

const registryDir = path.join(__dirname, 'registry', 'components');
const outputFile = path.join(__dirname, 'registry', 'source-map.ts');

const map = {};

if (fs.existsSync(registryDir)) {
  const categories = fs.readdirSync(registryDir, { withFileTypes: true });
  for (const category of categories) {
    if (!category.isDirectory()) continue;

    const categoryDir = path.join(registryDir, category.name);
    const files = fs.readdirSync(categoryDir);

    // Find every .tsx file in the category directory (e.g., ImageCarousel.tsx)
    for (const file of files) {
      if (file.endsWith('.tsx')) {
        const componentName = file.replace('.tsx', ''); // e.g. "ImageCarousel"
        const fullPath = path.join(categoryDir, file);
        map[componentName] = fs.readFileSync(fullPath, 'utf8');
      }
    }
  }
}

const keys = Object.keys(map);
console.log(`Generated source-map with ${keys.length} components: ${keys.join(', ')}`);

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by generate-registry-map.js to bundle component sources for Vercel Serverless Functions.
export const componentSourceMap: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;

fs.writeFileSync(outputFile, content);
console.log('Successfully written to registry/source-map.ts');
